import UIKit
import SwiftyJSON
import Alamofire
import CoreLocation

class ViewController: UIViewController {
    
    
    @IBOutlet weak var countryTextfield: UITextField!
    @IBOutlet weak var cityTextField: UITextField!
    @IBOutlet weak var streetTextField: UITextField!
    @IBOutlet weak var geocodeButton: UIButton!
    @IBOutlet weak var locationStatusLabel: UILabel!
    
    var geocoder = CLGeocoder()
    let dataWeather = DataWeather()
    

    override func viewDidLoad() {
        super.viewDidLoad()
        // Do any additional setup after loading the view, typically from a nib.
        
        
        
        
    }
    
    
    
    @IBAction func geocodeButtonPressed(_ sender: UIButton) {
        
        guard let country = countryTextfield.text else { return }
        guard let city = cityTextField.text else { return }
        guard let street = streetTextField.text else { return }
        
        let address = "\(country), \(city), \(street)"
        print(address)
        
        //geocode address string
        geocoder.geocodeAddressString(address) { (placemarks, error ) in
            self.prosesRespone(withPlacemarks: placemarks, error: error)
        }
        
        // update View
        geocodeButton.isHidden = true
        
        
    }
    
    func prosesRespone(withPlacemarks placemarks: [CLPlacemark]?, error: Error?){
        
        // update view
        geocodeButton.isHidden = false
        if let error = error {
            print("Lokasi Tidak Ditemukan")
            locationStatusLabel.text = "Lokasi Tidak Ditemukan"
        
        }else{
            var location: CLLocation?
            
            if let placemarks = placemarks, placemarks.count > 0 {
                location = placemarks.first?.location
            }
            
            if let location = location {
                let coordinate = location.coordinate
                let longitude = String(coordinate.longitude)
                let latitude = String(coordinate.latitude)
                
                // send data to dataweather
                dataWeather.long = longitude
                dataWeather.lat = latitude
                
                // store data to another function
                //let params: [String : String] = ["key": dataWeather.keyID, "long": longitude, "lat":latitude]
                
                
                let fullURL = dataWeather.urlWeather + "/" + dataWeather.keyID + "/" + latitude + "," + longitude
                
                processSendDataLocation(url: fullURL)
                
                print("full url : \(fullURL)")
                
                //processSendDataLocation(url: dataWeather.urlWeather, parameter: params)
                
                print("<<<<< longitude : \(longitude) \n Latitude : \(latitude)")
            }else{
                print("Tidak Ada Lokasi Yang Cocok")
                locationStatusLabel.text = "Tidak Ada Lokasi Yang Cocok"
            }
            
        }
        
        
    }
    
    
    func processSendDataLocation(url: String){
        
        Alamofire.request(url, method: .get).responseJSON{
            respone in
            if respone.result.isSuccess {
                print("sukses")
                let dataJSON: JSON = JSON(respone.result.value)
                print(dataJSON)
            }else if respone.result.isFailure{
                print("gagal koneksi")
                print(respone.result.error)
            }
        }
        
        
    }
    
    
    
    


}

